package manish.java;

public class RelationalOperators {
	
	public static void main(String[]args) {
		//== double equal
		//not equal!=
		// lessthan<
		// greater than >
		// <= less than equal to
		// >= greater than equal to
		
		int a = 65;
		System.out.println(a==65);
		System.out.println(a>67);
		System.out.println(a>=65);
		System.out.println(a!=66);
		System.out.println(a<=65);
				
		
	}

}
