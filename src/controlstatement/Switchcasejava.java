package controlstatement;

public class Switchcasejava {
	
	public static void main(String[]args) {
	String holiday = "Sunday";
	
	switch(holiday)
	{
	case "Monday":
	{
		System.out.println("This is monday");
		break; 
	}
	
	case "Tuesday":
	{
		System.out.println("This is Tuesday");
		break;
	}
	
	case "Wednesday":
	{
		System.out.println("This is wednesday");
		break;
	}
	
	case "Thurday":
	{
		System.out.println("This is Thurday");
		break;
	}
	
	case "Friday":
	{
		System.out.println("This is Friday");
		break;
	}
	
	case "Saturday":
	{
		System.out.println("This is Saturday");
		break;
	}
	
	//case "Sunday":
	//{
	//	System.out.println("Sunday is a Funday");
	//	break;
	//}
	case "Jan":
	{
		System.out.println("Jun");
	}
	case "Feb":
	{
		System.out.println("Feb");
		break;
	}
	case "March":
	{
		System.out.println("Sunday is a March");
		break;
	}
	default:
	{
		System.out.println("No days are found");
		break;
	}
		
	}
	}

}
